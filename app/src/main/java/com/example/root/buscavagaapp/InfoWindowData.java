package com.example.root.buscavagaapp;

public class InfoWindowData {

    private String nome;
    private String endereco;
    private String telefones;
    private String precosCarro;
    private String precosMoto;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getTelefones() {
        return telefones;
    }

    public void setTelefones(String telefones) {
        this.telefones = telefones;
    }

    public String getPrecosCarro() {
        return precosCarro;
    }

    public void setPrecosCarro(String precosCarro) {
        this.precosCarro = precosCarro;
    }

    public String getPrecosMoto() {
        return precosMoto;
    }

    public void setPrecosMoto(String precosMoto) {
        this.precosMoto = precosMoto;
    }
}
