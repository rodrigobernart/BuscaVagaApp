package com.example.root.buscavagaapp;

public class InfoWindowData {

    private String nome;
    private String precosCarro;
    private String precosMoto;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getPrecosCarro() {
        return precosCarro;
    }

    public void setPrecosCarro(String precosCarro) {
        this.precosCarro = precosCarro;
    }

    public String getPrecosMoto() {
        return precosMoto;
    }

    public void setPrecosMoto(String precosMoto) {
        this.precosMoto = precosMoto;
    }
}
